//Number within quotes cannot be added.
let num = '100';
let num1 = '2.2';
let total = num + num1;
console.log("String Numbers = " + total);

//Reusing existing variable
num = 100;
num1 = 2.2;
total = num + num1;
console.log("Numbers = " + total);

//Concating String
let firstName = 'Nikitha';
let lastName = 'Anthony';
console.log("Full Name - " + firstName + " " + lastName);
console.log("Full Name - " + firstName + "_" + lastName);